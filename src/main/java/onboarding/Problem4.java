package onboarding;

import java.util.Locale;

public class Problem4 {
    public static String solution(String word) {
        String answer = "";
        int ABC[] = new int[123];
        for(int i=0;i<26;i++){
            ABC[i+65]='Z'-i;
            ABC[i+97]='z'-i;
        }
       for(int i=0;i<word.length();i++){
           if(word.charAt(i)==' ')
           {
               answer+=' ';
               continue;
           }
           answer+=(char)ABC[word.charAt(i)];
       }
        return answer.trim();
    }

}
